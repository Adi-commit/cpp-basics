//Program to understand various types of Arithmetic and Logical operators
#include<iostream>
using namespace std;

int main(){
    //Arithmetic operators: +,-,/,*,%,++,--
    int a = 21;
    int b = 10;

    //Addition '+'
    cout<<"21 + 10 = "<<a+b;

    //Subtraction '-'
    cout<<endl<<"21 - 10 = "<<a-b;

    //Division '/'
    cout<<endl<<"21 / 10 = "<<a/b;

    //Multiplication '*'
    cout<<endl<<"21 * 10 = "<<a*b;

    //Modulo '%'
    cout<<endl<<"21 % 10 = "<<a%b;

    //Increment '++'
    cout<<endl<<"21 + 1 = "<<++a;

    //Decrement '--'
    cout<<endl<<"22 - 1 = "<<--a;
}